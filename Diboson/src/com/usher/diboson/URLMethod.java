package com.usher.diboson;

import java.io.Serializable;
import java.lang.reflect.Method;

public class URLMethod implements Serializable
{
	// =============================================================================
	// 18/09/2015 ECU created to pass a URL and method associated with it
	// =============================================================================
	private static final long serialVersionUID = 1L;
	// =============================================================================
	
	// =============================================================================
	String		URL;
	Method		URLMethod;
	// =============================================================================
	
	// =============================================================================
	public URLMethod (String theURL,Method theURLMethod)
	{
		// -------------------------------------------------------------------------
		// 18/09/2015 ECU constructor to set private parameters
		// -------------------------------------------------------------------------
		URL			= theURL;
		URLMethod	= theURLMethod;
		// -------------------------------------------------------------------------
	}
	// =============================================================================
}
