package com.usher.diboson;

import android.app.Service;
import android.content.Intent;
import android.os.IBinder;

public class WebServerService extends Service 
{
	// =============================================================================
	// 27/12/2014 ECU created to provide a web server to incoming requests
	// =============================================================================
	//private final static String TAG = "WebServerService";
	// =============================================================================
	private WebServer server = null;
	// =============================================================================
	@Override
	public void onCreate() 
	{
		super.onCreate();
		// -------------------------------------------------------------------------
		// 27/12/2014 ECU create an instance of the web server
		// -------------------------------------------------------------------------
		server = new WebServer (this);
		// -------------------------------------------------------------------------
	}
	// =============================================================================
	@Override
	public int onStartCommand (Intent intent, int flags, int startId) 
	{
		// -------------------------------------------------------------------------
		// 27/12/2014 ECU start up the defined web server
		// -------------------------------------------------------------------------
		server.startServer();
		// -------------------------------------------------------------------------
		// 27/12/2014 ECU want to try and bring back in memory
		// -------------------------------------------------------------------------
		return Service.START_STICKY;
		// -------------------------------------------------------------------------
	}
	// =============================================================================
	@Override
	public void onDestroy() 
	{
		// -------------------------------------------------------------------------
		// 27/12/2014 ECU stop the running server
		// -------------------------------------------------------------------------
		server.stopServer();
		// -------------------------------------------------------------------------
		super.onDestroy();
	}
	// =============================================================================
	@Override
	public IBinder onBind(Intent intent) 
	{
		return null;
	}
	// =============================================================================
}
